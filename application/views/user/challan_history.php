document.addEventListener('DOMContentLoaded', function () {
    const cpinRadio = document.getElementById('cpinRadio');
    const dateRadio = document.getElementById('dateRadio');
    const cpinDiv = document.getElementById('cpinSearchDiv');
    const dateDiv = document.getElementById('dateSearchDiv');

    function toggleSearchFields() {
        if (cpinRadio.checked) {
            cpinDiv.style.display = 'block';
            dateDiv.style.display = 'none';
        } else if (dateRadio.checked) {
            cpinDiv.style.display = 'none';
            dateDiv.style.display = 'block';
        } else {
            cpinDiv.style.display = 'none';
            dateDiv.style.display = 'none';
        }
    }

    // Listen to changes
    cpinRadio.addEventListener('change', toggleSearchFields);
    dateRadio.addEventListener('change', toggleSearchFields);

    // Initialize on page load (in case of old selection)
    toggleSearchFields();
});
</script>



